package simpleprogram;

public class StaticCalling {
	static void add () {
		System.out.println("priyanka love sahil");
	}
	public static void main (String [] args) {
		System.out.println("sahil love priyanka");
		add();
		sub();
		
	}
	 static void sub() {
		System.out.println("i love you priyanka");
	 }
}
