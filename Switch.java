package simpleprogram;

public class Switch {
	public static void main(String[] args) {
		switch (5)
		{
		case 1:System.out.println("launch chrome browser");
			break;
		case 2:System.out.println("launch edge browser");
		break;
		case 3:System.out.println("launch firefox browser");
		break;
		case 4:System.out.println("launch internet explorer browser");
		break;
		case 5:System.out.println("launch safari browser");
		break;
		default:
			System.out.println("please enter 1,2,3,4 or 5 to launch the browser ");
		
		
		
		}
	}

}
